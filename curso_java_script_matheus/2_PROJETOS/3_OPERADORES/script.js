
/*operações aritméticas*/

console.log(2 + 3)  // ADIÇÃO
console.log(5 - 12) // SUBTRAÇÃO
console.log(4 * 4)  // MULTIPLICAÇÃO
console.log(Math.round((5/3)))  // DIVISÃO
console.log(23 % 2) // RESTO DA DIVISÃO

/*tipo de dados que obtemos no resultado das operações*/

console.log(typeof(2 + 3))  // ADIÇÃO
console.log(typeof(5 - 12)) // SUBTRAÇÃO
console.log(typeof(4 * 4))  // MULTIPLICAÇÃO
console.log(typeof(Math.round(5/3)))  // DIVISÃO
console.log(typeof(23 % 2)) // RESTO DA DIVISÃO
