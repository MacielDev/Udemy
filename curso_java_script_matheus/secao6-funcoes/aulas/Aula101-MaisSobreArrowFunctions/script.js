
//Determinando Arrow Functions com um único Argumaneto

//Forma extensa
const raiQuadrada =(x)=>{
    return Math.sqrt(x)
}

console.log(`A raiz quadradado valor informado é igual a ${raiQuadrada(16)}`)

//Forma reduzida

const raizQuadradaFormaReduzida = n => Math.sqrt(n)


console.log(`A raiz quadrada do valor informado é igual a ${raizQuadradaFormaReduzida(9)}`)